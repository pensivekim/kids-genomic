export const curriculum: Lesson[] = BASE.flatMap((base) =>
  ([3, 4, 5] as Age[]).map((age) => ({
    ...base,
    age,
    ...AGE_CONTENT[base.week][age],
  }))
)

// ─────────────────────────────────────────────
// 헬퍼 함수
// ─────────────────────────────────────────────

/** 연령별 전체 수업 목록 */
export function getLessonsByAge(age: Age): Lesson[] {
  return curriculum.filter((l) => l.age === age)
}

/** 현재 월 + 연령에 맞는 수업 (첫 번째 주) */
export function getCurrentLesson(age: Age, month?: number): Lesson {
  const m = month ?? new Date().getMonth() + 1
  const lessons = getLessonsByAge(age)
  return lessons.find((l) => l.month === m) ?? lessons[0]
}

/** 특정 주차 + 연령 수업 */
export function getLessonByWeek(age: Age, week: number): Lesson | undefined {
  return curriculum.find((l) => l.age === age && l.week === week)
}
